    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        StudentDAO dao = new StudentDAO(); 
        Student    s   = new Student();    
       id = idfield.getText();
       if(id.equalsIgnoreCase("")){
       JOptionPane.showMessageDialog(rootPane, " ID Must Be Inputed !");
       } else{
           s.setId(idfield.getText());
       boolean check = dao.checkStudentID(s);
       
       if(check != true){
       s =dao.getStudentInfo(idfield.getText());
      jta.setText("\n\n   ID \t : "+s.getId()
              + "\n \n Name \t : "+s.getName()
              + "\n \n Father's Name \t : "+s.getFname()
              + "\n\n Address \t : " +s.getAddress()
              + "\n\n Class \t :  "+s.getClas()
              + "");
      uid.setText(s.getId());
       } else{
       JOptionPane.showMessageDialog(rootPane, "Student ID : "+idfield.getText()+" \t not found !");
       }
       
       }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

