            con=db.getConnection();
            stmt=con.prepareStatement("UPDATE student SET name = \'"+ namefield.getText()+"\', fname =\'"+fnamefield.getText()+"\', address = \'"+addrfield.getText()+"\', clas =\'"+clasfield.getText() +"\'  WHERE id = \'"+uid.getText()+ "\'");
