    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       
      
        DBconnect db = new  DBconnect();
        StudentDAO dao = new StudentDAO();
        Student s = new Student();
        Connection con = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;
        String id = idfield.getText();
        if(id.equalsIgnoreCase("")){
        JOptionPane.showMessageDialog(rootPane,"ID can't be empty !");
        }else{
            s.setId(id);
            boolean status = dao.checkStudentID(s);
            
        if(status != true){
        
        try {
            con = db.getConnection();

            stmt = con.prepareStatement("select * from fees  where id=\'"+s.getId()+"\'");
            rs = stmt.executeQuery();
           

        while (rs.next()) {
            
         ta1.setText( "\t \t IDB-BISEW School \n \n"
                 + "\n \n ID \t: "+rs.getString("id")
                 + "\n \n Admission Fee : "+rs.getDouble("admfee")
                 + "\n \n Exam Fee \t: "+rs.getDouble("examfee")
                 + "\n \n Sport Fee \t: "+rs.getDouble("sportfee")
                 + "\n \n Others Fee \t: "+rs.getDouble("exfee")
                 
                 + "");                       
                      
         
            }
        
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            db.close(con, stmt, null);
        }
        
        
        }else {
            
        JOptionPane.showMessageDialog(rootPane," There is no Student ! ");        
                
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
